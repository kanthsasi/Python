# Python program to show how if-else statements work  
  
string = "thanav"  
  
# Initiating a loop  
for s in  string:  
    # giving a condition in if block  
    if s == "a":  
        print("If block")  
    # if condition is not satisfied then else block will be executed  
    else:  
        print(s)
print("------------------------------------------------------------------")

